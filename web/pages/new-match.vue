<template>
  <v-form
    v-model="valid"
    ref="form"
    lazy-validation
  >
    <v-container>
      <v-layout row wrap>
        <v-flex xs12 md6>
          <h6 class="subheading">Heim</h6>
          <v-layout row justify-space-between>
            <v-flex xs8>
              <v-autocomplete
                v-model="homePlayers"
                :items="players"
                :rules="[requiredArray, differentFromAwayPlayers]"
                item-text="user.username"
                item-value="id"
                label="Spieler"
                box
                chips
                deletable-chips
                clearable
                multiple
              ></v-autocomplete>
            </v-flex>
            <v-flex xs3>
              <v-text-field
                v-model="homeScore"
                :rules="scoreRules"
                :disabled="sets.length > 0"
                label="Punkte"
                type="number"
                required
              ></v-text-field>
            </v-flex>
          </v-layout>
        </v-flex>

        <v-flex xs12 md6>
          <h6 class="subheading">Gast</h6>
          <v-layout row justify-space-between>
            <v-flex xs8>
              <v-autocomplete
                v-model="awayPlayers"
                :items="players"
                :rules="[requiredArray, differentFromHomePlayers]"
                item-text="user.username"
                item-value="id"
                label="Spieler"
                box
                chips
                deletable-chips
                clearable
                multiple
              ></v-autocomplete>
            </v-flex>
            <v-flex xs3>
              <v-text-field
                v-model="awayScore"
                :rules="scoreRules"
                :disabled="sets.length > 0"
                label="Punkte"
                type="number"
                required
              ></v-text-field>
            </v-flex>
          </v-layout>
        </v-flex>

        <v-flex xs12>
          <h6 class="subheading">Sätze (optional)</h6>
          <v-card
            v-for="(set, index) in sets"
            :key="index"
            class="mt-4"
          >
            <v-container grid-list-lg fluid>
              <v-layout row wrap justify-space-between>
                <v-flex xs2 class="my-auto">
                  <span>Heim</span>
                </v-flex>
                <v-flex xs7>
                  <v-radio-group
                    v-model="set.homeColor"
                    row
                  >
                    <v-radio
                      label="rot"
                      color="red"
                      value="red"
                    />
                    <v-radio
                      label="blau"
                      color="blue"
                      value="blue"
                    />
                  </v-radio-group>
                </v-flex>
                <v-flex xs3>
                  <v-text-field
                    v-model="set.homePoints"
                    label="Tore"
                    type="number"
                    required
                  ></v-text-field>
                </v-flex>
                <v-flex xs2 class="my-auto">
                  <span>Gast</span>
                </v-flex>
                <v-flex xs7>
                  <v-radio-group
                    v-model="set.awayColor"
                    row
                  >
                    <v-radio
                      label="rot"
                      color="red"
                      value="red"
                    />
                    <v-radio
                      label="blau"
                      color="blue"
                      value="blue"
                    />
                  </v-radio-group>
                </v-flex>
                <v-flex xs3>
                  <v-text-field
                    v-model="set.awayPoints"
                    label="Tore"
                    type="number"
                    required
                  ></v-text-field>
                </v-flex>
              </v-layout>
              <v-btn
                fab
                absolute
                top
                right
                round
                small
                @click="removeSet(index)"
              >
                <v-icon>mdi-delete</v-icon>
              </v-btn>
            </v-container>
          </v-card>
          <div class="mt-3">
            <v-btn
              round
              @click="addSet()"
            >
              <v-icon left>mdi-plus</v-icon>
              neuer satz
            </v-btn>
          </div>
        </v-flex>

        <v-flex xs12>
          <v-layout row justify-end>
            <v-btn to="/">Zurück</v-btn>
            <v-btn
              :disabled="!valid"
              color="primary"
              @click="submit"
            >
              Eintragen
            </v-btn>
          </v-layout>
        </v-flex>
      </v-layout>
    </v-container>
  </v-form>
</template>

<script>
import { mapState, mapActions } from 'vuex'

export default {
  data() {
    return {
      homePlayers: [],
      awayPlayers: [],
      homeScore: undefined,
      awayScore: undefined,
      valid: true,
      sets: [],
    }
  },
  methods: {
    addSet() {
      this.sets.push({
        homePoints: 0,
        awayPoints: 0,
        homeColor: '',
        awayColor: '',
      })
    },
    removeSet(index) {
      this.sets = this.sets.filter((set, sIndex) => index != sIndex)
    },
    async submit() {
      if (!this.$refs.form.validate()) {
        return
      }

      await this.addMatchResult({
        homePlayers: this.homePlayers,
        awayPlayers: this.awayPlayers,
        homeScore: this.homeScore,
        awayScore: this.awayScore,
        sets: this.sets,
      })

      this.$router.push('/')
    },
    ...mapActions({
      addMatchResult: 'addMatchResult',
    })
  },
  watch: {
    sets: {
      deep: true,
      handler() {
        this.homeScore = this.sets.filter((s) => s.homePoints >= s.awayPoints).length
        this.awayScore = this.sets.filter((s) => s.homePoints <= s.awayPoints).length
        this.sets.forEach((set) => {
          if (set.homeColor == 'red') {
            set.awayColor = 'blue'
          }
          if (set.homeColor == 'blue') {
            set.awayColor = 'red'
          }
          if (set.awayColor == 'red') {
            set.homeColor = 'blue'
          }
          if (set.awayColor == 'blue') {
            set.homeColor = 'red'
          }
        })
      },
    },
  },
  computed: {
    differentFromAwayPlayers() {
      return (players) => !players.some((player) => this.awayPlayers.includes(player)) || 'Darf nicht in mehreren Teams auftauchen'
    },
    differentFromHomePlayers() {
      return (players) => !players.some((player) => this.homePlayers.includes(player)) || 'Darf nicht in mehreren Teams auftauchen'
    },
    requiredArray() {
      return (players) => players.length > 0 || 'Darf nicht leer sein'
    },
    required() {
      return (input) => (input !== undefined && input !== '') || 'Pflichtfeld'
    },
    scoreRules() {
      return [
        this.required,
        (input) => input >= 0 || 'Darf nicht negativ sein',
      ]
    },
    ...mapState({
      players: (state) => state.players,
    })
  },
}
</script>

